# print() function display values on the screen

# 1. display string or characters
print("Hello world")
print("H")

# 2. display the integers and floating point values
print(300) # display integer values
print(675.95) # display floating point values

# 3. integers values with a string
print(89,"people went to Nairobi")

# 4. display value stored in a variable
y = 100
print(y)

# 5. f-string -  helps display values differently.
# syntax :  print(f"{variable_name }"")
l = 10
w = 16
print(f"The area is {l} x {w} = {l * w}")


# 6. str.format()
# syntax: print("()".format())
length = 50
width = 45
height = 40
print("{}x{}x{} = {}".format(length, width, height, length*width*height))

