def rectangle(length, width):
    area = length * width
    return area # last statement is return

"""
instance variable
> It holds the function call 
> It also hold the return value after the function has been executed
"""
instance_variable = rectangle(88, 45)  # instance_variable = area
print(instance_variable)




def square(side):
    area = side * side
    print(area) # last statement is print

square(78)


