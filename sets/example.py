# set eliminates duplicates in items
basket = {'apple', 'orange', 'apple', 'pear', 'orange', 'banana'}
print(basket)

# You can use membership operators on sets e.g (in , not in).
print("orange" in basket)
print("lemon" in basket)

# Use the set() function in creating set out of a string of words.
a = set('abracadabra')
b = set('alacazam')
print(a)
print(b)

# subtraction(gives you items in first set which are not in second set)
new_set = a - b
print(new_set)









