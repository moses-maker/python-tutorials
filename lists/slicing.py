# SLICING IN LISTS

years = [2000, 2001, 2002, 2003, 2004, 2005, 2006, 2007, 2008, 2009, 2010]
# stepping with 2
my = years[0:8:2]
print(my)

# extract from index 2 and the rest
my2 = years[2:]
print(my2)

# extract from items in index 0, 1, 2
my3 = years[:3]
print(my3)

my4 = years[::2]
print(my4)

#print list in reverse
my5 = years[::-1]
print(my5)

my6 = years[-3:-1]
print(my6)

